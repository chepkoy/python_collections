# Adding lists together
favourite_subjects = ['math','geography', 'physics']
print(favourite_subjects + ['Biochemistry'])

# Using append
favourite_subjects.append("zoology")

print(favourite_subjects)

# Using extend
favourite_subjects.extend(["quantum chemistry", "Physical chemistry"])

print(favourite_subjects)

# Using insert

favourite_subjects.insert(1, 'botany')
print(favourite_subjects)



